package com.prashant;

public class Dimensions {
    private int weidth;
    private int height;
    private int depth;

    //constructor
    public Dimensions(int weidth,int height,int depth){
        this.weidth = weidth;
        this.height = height;
        this.depth = depth;
    }

    //getter

    public int getWeidth() {
        return weidth;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }


}
