package com.prashant;

//native resolution
public class Resolution {
    private int width;
    private int height;

    //constructor
    public Resolution(int width,int height){
        this.width = width;
        this.height = height;
    }
    //getters
    public int getWidth(){
        return width;
    }
    public int getHeight(){
        return height;
    }
}
